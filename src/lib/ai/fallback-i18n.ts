import { ConversationIntent, type LanguageCode } from "@/types";

/**
 * Localized offline fallback replies.
 *
 * Used when no AI provider key is configured (demo mode) or a provider call
 * fails. This guarantees the assistant visibly responds in the language the
 * user selected, instead of always replying in English.
 *
 * When a real AI provider is configured, the model handles full multilingual
 * answers and these are only a safety net.
 */

type FallbackKey = "default" | "scheme" | "product";

export function intentToFallbackKey(intent: ConversationIntent): FallbackKey {
  if (intent === ConversationIntent.SchemeDiscovery) return "scheme";
  if (intent === ConversationIntent.ProductRecommendation) return "product";
  return "default";
}

